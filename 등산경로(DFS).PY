dx = [0, 1, 0, -1]
dy = [-1, 0, 1, 0]

def DFS(x, y):
    global cnt
    if x==ex and y==ey:
        cnt += 1
        return
    else:
        for k in range(4):
            xx = x + dx[k]
            yy = y + dy[k]
            if 0 <= xx < n and 0 <= yy < n and ch[xx][yy] == 0 and board[xx][yy] > board[x][y]:
                ch[xx][yy] = 1
                DFS(xx, yy)
                ch[xx][yy] = 0


if __name__ == "__main__":
    n = int(input())
    board = [[0] * n for _ in range(n)]
    ch = [[0] * n for _ in range(n)]
    mx = -2147000000
    mn = 2147000000
    cnt = 0
    for i in range(n):
        tmp = list(map(int, input().split()))
        for j in range(n):
            if tmp[j] < mn:
                mn = tmp[j]
                sx = i
                sy = j
            if tmp[j] > mx:
                mx = tmp[j]
                ex = i
                ey = j
            board[i][j] = tmp[j]
    ch[sx][sy] = 1
    DFS(sx, sy)
    print(cnt)
                

    
    


    

